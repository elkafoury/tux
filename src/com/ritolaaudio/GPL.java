package com.ritolaaudio;

/**
 * A simple helper class for printing GPL legal material without cluttering the implementing source code.
 * @author chuck
 *
 */
public class GPL
	{
	/**
	 * Print the GPL v3 disclaimer with the supplied program name and copyright year(s).
	 * @param programName
	 * @param year		Year, years, or year range (i.e. "2011", "2010, 2011", "1965-1974" or any permutation thereof.
	 * @param authorNames	Full names of authors, separated with commas with an 'and' separator if a plurality. Ends with no terminating punctuation. 
	 * (i.e. "Chuck Ritola, Foobar McBarfoo, and Trollface Trolol")
	 * @author	Chuck Ritola
	 * @date	Oct 6, 2011
	 */
	public static void printDisclaimer3(String programName, String year, String authorNames)
		{
		System.out.println(programName+" Copyright (C) "+year+" "+authorNames+".\n" +
				"This program comes with ABSOLUTELY NO WARRANTY. \n"+
				"This is free software, and you are welcome to redistribute it\n"+
				"under certain conditions of the GPL version 3. \n"+
				"See the included file named 'COPYING' for details.\n");
		}
	}//end GPL
